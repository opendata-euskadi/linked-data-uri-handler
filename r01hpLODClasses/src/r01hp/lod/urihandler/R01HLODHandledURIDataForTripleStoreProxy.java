package r01hp.lod.urihandler;

import lombok.Getter;
import lombok.experimental.Accessors;
import r01f.types.url.UrlPath;
import r01f.util.types.Strings;

@Accessors(prefix="_")
public class R01HLODHandledURIDataForTripleStoreProxy
	 extends R01HLODHandledURIDataBase {
/////////////////////////////////////////////////////////////////////////////////////////
//	FIELDS
/////////////////////////////////////////////////////////////////////////////////////////
	@Getter private final UrlPath _urlPathToBeProxied;
/////////////////////////////////////////////////////////////////////////////////////////
//	CONSTRUCTOR
/////////////////////////////////////////////////////////////////////////////////////////
	public R01HLODHandledURIDataForTripleStoreProxy(final UrlPath urlPathToBeProxied) {
		super(R01HLODURIHandleAction.PROXY_TRIPLE_STORE);
		_urlPathToBeProxied = urlPathToBeProxied;
	}
/////////////////////////////////////////////////////////////////////////////////////////
//	DEBUG
/////////////////////////////////////////////////////////////////////////////////////////	
	@Override
	public CharSequence debugInfo() {
		return Strings.customized("Proxy triple-store {}",
								  _urlPathToBeProxied.asAbsoluteString());
	}
}
